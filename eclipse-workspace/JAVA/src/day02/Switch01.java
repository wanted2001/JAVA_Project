package day02;

public class Switch01 {

	public static void main(String[] args) {
		/*
		 * switch문 : 값에 따라 케이스를 달리주는 형식
		 * 
		 * switch(변수/식) {
		 * case 값 1: 실행문; break;
		 * case 값 2: 실행문; break;
		 * case 값 3: 실행문; break;
		 * case 값 4: 실행문; break;
		 * case 값 5: 실행문; break;
		 * case 값 6: 실행문; break;
		 * default: 실행문; break;
		 * }
		 * 
		 * break; switch문을 빠져나갈때 사용
		 */

		int num = 5 ;
		
		switch(num) {
		case 6: System.out.println(num--);
		case 5: System.out.println(num--);
		case 4: System.out.println(num--);
		case 3: System.out.println(num--);
		break;
		default: System.out.println("잘못 입력된 값입니다.");
		break;
		}
		
		int num2 = 1; //1,2,3 중 하나
		
		switch (num2) {
		case 1: System.out.println("1입니다."); //break;
		case 2: System.out.println("2입니다."); //break;
		case 3: System.out.println("3입니다."); //break;
		}
		
	}

}
